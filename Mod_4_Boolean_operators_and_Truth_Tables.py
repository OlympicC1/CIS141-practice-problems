#AND Operators
#A      #B             A AND B
#------------------------------
#True True True
#True  False  False
#False True   False
#False False  False

#print(True and True)  #True
#print(True and False) #False


#OR Operator
#A     B        A OR B
#-----------------------
#True  True  True
#True  False True
#False True  True
#False False False

#print(True or False) #True
#print(False or False) # False (|| = OR)

#NOT Operator
#Toggles true -> false or false -> true)
#A     NOT A
#------------
#True  False
#False True

#True  True  True
#True  False True
#print(not True)   #False
#print(not False)   #True

#XOR Operator
#A     B      A XOR B
#----------------------
#False False False
#False True  True
#True  False True
#True  True  True

#print(True ^ False)  #True
#print(True ^ True)  #False
